#include <sys/types.h>
#include <sys/sysctl.h>
#include <stddef.h>

int getdtablecount(void);

/* 
 * Return the count of open file descriptors for this process.
 *
 */
int
getdtablecount(void)
{
	int mib[4];
	int error;
	int nfds;
	size_t len;

	len = sizeof(nfds);
	mib[0] = CTL_KERN;
	mib[1] = KERN_PROC;
	mib[2] = KERN_PROC_NFDS;
	mib[3] = 0;

	error = sysctl(mib, 4, &nfds, &len, NULL, 0);
	if (error)
		return (-1);
	return (nfds);
}
