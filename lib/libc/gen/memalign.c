#include <sys/param.h>
#include <stdlib.h>

void *
memalign(size_t align, size_t size)
{
	/*
	 * glibc allows align == 0, but that is not valid for roundup.
	 * Just pass through to malloc in that case.
	 */
	if (align != 0)
		return (aligned_alloc(align, roundup(size, align)));
	else
		return (malloc(size));
}
