/*
 * Return the starting address of string S2 in S1;
 * return 0 if it is not found.
 */
char *
strstr(char *s1, char *s2)
{
  int i;
  char *p1;
  char *p2;
  char *s = s1;

  for (p2 = s2, i = 0; *s; p2 = s2, i++, s++) {
    for (p1 = s; *p1 && *p2 && *p1 == *p2; p1++, p2++)
      ;
    if (!*p2)
      break;
  }
  if (!*p2)
    return s1 + i;

  return 0;
}
