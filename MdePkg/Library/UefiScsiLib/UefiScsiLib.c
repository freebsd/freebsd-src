  // Fill Cdb for Security Protocol In Command
  //
  Cdb[0]                        = EFI_SCSI_OP_SECURITY_PROTOCOL_IN;
  Cdb[1]                        = SecurityProtocol;
  WriteUnaligned16 ((UINT16 *)&Cdb[2], SwapBytes16 (SecurityProtocolSpecific));

