#ifndef _AQ_COMMON_H_
#define _AQ_COMMON_H_

#include <sys/types.h>


#define BIT(nr) (1UL << (nr))

#define AQ_HW_WAIT_FOR(_B_, _US_, _N_) ({ \
	unsigned int _i; \
	for (_i = (_N_); !(_B_) && _i; --_i) \
		DELAY(_US_); \
	(_i == 0) ? ETIMEDOUT : 0; \
})


#define AQ_VER        "0.0.5"

#endif // _AQ_COMMON_H_
