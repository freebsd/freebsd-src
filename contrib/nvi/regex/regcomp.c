/*
 - enlarge - enlarge the strip
 */
static int
enlarge(struct parse *p, sopno size)
{
	sop *sp;
	RCHAR_T *dp;
	sopno osize;

	if (p->ssize >= size)
		return 1;

	osize = p->ssize;
	p->ssize = size;
	if (MEMSIZE(p) > MEMLIMIT)
		goto oomem;
	sp = realloc(p->strip, p->ssize * sizeof(sop));
	if (sp == NULL)
		goto oomem;
	p->strip = sp;
	dp = realloc(p->stripdata, p->ssize * sizeof(RCHAR_T));
	if (dp == NULL) {
oomem:
		p->ssize = osize;
		SETERROR(REG_ESPACE);
		return 0;
	}
	p->stripdata = dp;
	return 1;
}

