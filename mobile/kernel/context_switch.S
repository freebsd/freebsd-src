	.text
	.globl context_switch
	.type context_switch,@function
context_switch:
	# a0 = old context pointer, a1 = new context pointer

	# Save old context registers
	sd ra, 0(a0)
	sd sp, 8(a0)
	sd gp, 24(a0)
	sd tp, 32(a0)
	sd t0, 40(a0)
	sd t1, 48(a0)
	sd t2, 56(a0)
	sd s0, 64(a0)
	sd s1, 72(a0)
	sd a0, 80(a0)
	sd a1, 88(a0)
	sd a2, 96(a0)
	sd a3, 104(a0)
	sd a4, 112(a0)
	sd a5, 120(a0)
	sd a6, 128(a0)
	sd a7, 136(a0)
	sd s2, 144(a0)
	sd s3, 152(a0)
	sd s4, 160(a0)
	sd s5, 168(a0)
	sd s6, 176(a0)
	sd s7, 184(a0)
	sd s8, 192(a0)
	sd s9, 200(a0)
	sd s10, 208(a0)
	sd s11, 216(a0)
	sd t3, 224(a0)
	sd t4, 232(a0)
	sd t5, 240(a0)
	sd t6, 248(a0)
	sd ra, 16(a0)

	# Restore new context registers
	ld ra, 16(a1)
	ld sp, 8(a1)
	ld gp, 24(a1)
	ld tp, 32(a1)
	ld t0, 40(a1)
	ld t1, 48(a1)
	ld t2, 56(a1)
	ld s0, 64(a1)
	ld s1, 72(a1)
	ld a0, 80(a1)
	ld a1, 88(a1)
	ld a2, 96(a1)
	ld a3, 104(a1)
	ld a4, 112(a1)
	ld a5, 120(a1)
	ld a6, 128(a1)
	ld a7, 136(a1)
	ld s2, 144(a1)
	ld s3, 152(a1)
	ld s4, 160(a1)
	ld s5, 168(a1)
	ld s6, 176(a1)
	ld s7, 184(a1)
	ld s8, 192(a1)
	ld s9, 200(a1)
	ld s10, 208(a1)
	ld s11, 216(a1)
	ld t3, 224(a1)
	ld t4, 232(a1)
	ld t5, 240(a1)
	ld t6, 248(a1)
	ld t0, 0(a1)
	jr t0
	.size context_switch, .-context_switch
